public class Triangle{
    private int height;
    private int base;
    private static int countTriangle=0;
    
    public Triangle(int height, int base){
        this.height = height;
        this.base = base;
        this.countTriangle++;
    }
    
    public boolean isEnough(){
        if(this.countTriangle>=5)
            return true;
        else return false;
    }
    
    public boolean isEnough(int enoughNumber){
        if(this.countTriangle>=enoughNumber)
            return true;
        else return false;
    }
}
